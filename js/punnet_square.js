function chancecalc(phenspisok) {
  let binom = 0;
  for (let k = 0; k < spisokvar.length; k++) {
    if (phenspisok.includes(spisokvar[k].variant)) {
      binom += spisokvar[k].percentage;
    }
  }
  return binom;
}
function factorialize(num) {
  var result = num;
  if (num === 0 || num === 1) return 1;
  while (num > 1) {
    num--;
    result *= num;
  }
  return result;
}
