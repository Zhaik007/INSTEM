function PossibilityCounter() {
  let bin = document.querySelector("#chances").innerHTML.toString();
  let bino = bin.split(" ");
  let bnom = bino.filter(function (el) {
    return !isNaN(el);
  });
  let binom = bnom.map(function (val) {
    return parseFloat(val);
  });
  let k = Number(document.querySelector("#kun").value);
  let n = Number(document.querySelector("#nun").value);
  let possibility = 0;
  for (let i = 0; i < k; i++) {
    possibility +=
      (factorialize(n) / factorialize(n - i) / factorialize(i)) *
      Math.pow(binom / 100, i) *
      Math.pow(1 - binom / 100, n - i);
  }
  document.querySelector("#kun").value = "";
  document.querySelector("#nun").value = "";
  if (
    isNaN(k) ||
    isNaN(n) ||
    n == 0 ||
    k == 0 ||
    isNaN(binom) ||
    k > n ||
    isNaN((1 - possibility) * 100)
  ) {
    document.querySelector("#Possibility").textContent = `Вероятность 0 %`;
  } else {
    document.querySelector("#Possibility").textContent = `Вероятность ${(
      (1 - possibility) *
      100
    ).toFixed(2)} %`;
  }
}

